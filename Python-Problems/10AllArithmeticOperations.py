# Take two numbers from user
a = int(input("Enter first number: "))
b = int(input("Enter second number: "))

# Arithmetic operations
print("Addition:", a + b )        # + adds two numbers
print("Subtraction:", a - b)     # - subtracts
print("Multiplication:", a * b)  # * multiplies
print("Division:", a / b)        # / gives float result
print("Floor Division:", a // b) # // gives integer division
print("Modulus:", a % b)         # % gives remainder
print("Power:", a ** b)          # ** gives a raised to power b
